# Float Variables
num1 = 5.0
num2 = 2.5

# Addition of num1 & num2

result1 = num1 + num2
print ("Addition:", result1)

# Substraction of num1 & num2

result2 = num1 - num2
print ("Substraction:", result2)

# Multiplication of num1 & num2

result3 = num1 * num2
print ("Multiplication:", result3)


# Division of num1 & num2

result4 = num1 / num2
print ("Division:", result4)
